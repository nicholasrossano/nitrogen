/**
 * Get a lucide-react icon component by name
 * Returns FolderOpen as fallback if icon name not found
 */
export function getIconByName(iconName: string | null | undefined): LucideIcon {
  if (!iconName) return FolderOpen;
  return iconMap[iconName] || FolderOpen;
}
